class Solution:
    def addStrings(self, num1: str, num2: str) -> str:
        #num1 = "11"
        #num2 = "123"
        
        n1 = int(num1)
        n2 = int(num2)
        
        sum1 = str(n1 + n2)
        
        return(sum1)

"""
Example 1:

Input: num1 = "11", num2 = "123"
Output: "134"
"""